export function calcAskrida(_tglLhr, _tglMulai, _jw, _umur, _plafon): number {
  const tglLhr = moment(new Date(_tglLhr));
  const tglMulai = moment(new Date(_tglMulai));
  const uBln =
    tglMulai.month() < tglLhr.month()
      ? tglMulai.diff(tglLhr.year(tglMulai.year() - 1), "months")
      : tglMulai.diff(tglLhr.year(tglMulai.year()), "months");
  const uHari =
    tglLhr.date() <= tglMulai.date()
      ? Math.abs(tglMulai.date() - tglLhr.date())
      : Math.abs(
          tglMulai.diff(
            moment([tglMulai.year(), tglMulai.month() - 1, tglLhr.date()]),
            "days"
          )
        );
  const usia =
    uBln < 6 ? _umur : uBln == 6 ? (uHari > 0 ? _umur + 1 : _umur) : _umur + 1;
  let jw: number =
    _jw / 12 == 0
      ? _jw / 12
      : _jw % 12 < 7
      ? roundTo.down(_jw / 12, 0)
      : roundTo.down(_jw / 12, 0) + 1;
  const rate =
    asuransi.askrida().find(f => f.usia == usia)["jw" + jw.toString()] || 0;
  return roundTo((_plafon * roundTo(rate, 2)) / 1000, 0);
}

