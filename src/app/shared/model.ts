export function convertDate(tgl: any, yearFirst: boolean = false) {
  function pad(s) {
    return s < 10 ? "0" + s : s;
  }
  let d = new Date(tgl);
  return yearFirst
    ? [d.getFullYear(), pad(d.getMonth() + 1), pad(d.getDate())].join("-")
    : [pad(d.getMonth() + 1), pad(d.getDate()), d.getFullYear()].join("-");
}

